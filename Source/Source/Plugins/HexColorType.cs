namespace Marklight
{
    /// <summary>
    /// Specifies the arrangment of color components on a hexidecimal color value.
    /// </summary>
    public enum HexColorType
    {
        /// <summary>
        /// Color components are arranged alpha, red, green, blue.
        /// </summary>
        ARGB,
        /// <summary>
        /// Color components are arranged red, green, blue, alpha.
        /// </summary>
        RGBA
    }
}